package com.company.day02.java_arrays.example02;

public class Subject {
    private String nameSubject;
    private double factorSubject;
    private double point;

    public Subject() {

    }

    public String getNameSubject() {
        return nameSubject;
    }

    public void setNameSubject(String nameSubject) {
        this.nameSubject = nameSubject;
    }

    public double getFactorSubject() {
        return factorSubject;
    }

    public void setFactorSubject(double factorSubject) {
        this.factorSubject = factorSubject;
    }

    public double getPoint() {
        return point;
    }

    public void setPoint(double point) {
        this.point = point;
    }

}
